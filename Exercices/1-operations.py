# Déclaration de deux variables entières
a = 15
b = 4

# Addition
somme = a + b
print("Addition: 15 + 4 =", somme) #output: 19

# Soustraction
difference = a - b
print("Soustraction: 15 - 4 =", difference) #output: 11

# Multiplication
produit = a * b
print("Multiplication: 15 * 4 =", produit) #output: 60

# Division
quotient = a / b
print("Division: 15 / 4 =", quotient) #output: 3.75

# Division entière (quotient entier)
quotient_entier = a // b
print("Division entière: 15 // 4 =", quotient_entier) #output: 3

# Exponentiation (puissance)
puissance = a ** b
print("Exponentiation: 15 ** 4 =", puissance) #output: 50625

# Modulo (reste de la division)
reste = a % b
print("Modulo: 15 % 4 =", reste) #output: 3 

# Opérations d'affectation combinées
# Ajout et affectation
a += 5  # équivalent à a = a + 5
print("Après a += 5, a =", a) #output: 20

# Soustraction et affectation
a -= 3  # équivalent à a = a - 3
print("Après a -= 3, a =", a) #output: 17

# Multiplication et affectation
a *= 2  # équivalent à a = a * 2
print("Après a *= 2, a =", a) #output: 34

# Division et affectation
a /= 4  # équivalent à a = a / 4
print("Après a /= 4, a =", a) #output: 8.5

# Modulo et affectation
a %= 3  # équivalent à a = a % 3
print("Après a %= 3, a =", a) #output: 2.5
