# Déclaration d'une variable de type entier (integer)
# Les entiers sont des nombres sans décimale
nombre_entier = 10
# Affiche la valeur de la variable nombre_entier
print("Le nombre entier est:", nombre_entier) #output: 10

# Déclaration d'une variable de type flottant (float)
# Les flottants sont des nombres avec une partie décimale
nombre_flottant = 10.5
# Affiche la valeur de la variable nombre_flottant
print("Le nombre flottant est:", nombre_flottant) #output: 10.5

# Déclaration d'une variable de type chaîne de caractères (string)
# Les chaînes de caractères sont des séquences de caractères entre guillemets
chaine_de_caracteres = "Bonjour, tout le monde!"
# Affiche la valeur de la variable chaine_de_caracteres
print("La chaîne de caractères est:", chaine_de_caracteres) #output: Bonjour, tout le monde!

# Déclaration d'une variable de type booléen (boolean)
# Les booléens peuvent être True (vrai) ou False (faux)
est_vrai = True
# Affiche la valeur de la variable est_vrai
print("La valeur booléenne est:", est_vrai) #output: True

# On peut changer la valeur d'une variable en lui assignant une nouvelle valeur
nombre_entier = 20
print("Le nouveau nombre entier est:", nombre_entier) #output: 20

# Exemple de concaténation de chaînes de caractères
prenom = "Alice"
nom = "Dupont"
# Concatène les deux chaînes avec un espace entre elles
nom_complet = prenom + " " + nom
print("Le nom complet est:", nom_complet) #output: Alice Dupont

# Exemple d'utilisation de variables dans une opération arithmétique
# Ajoute un entier et un flottant
resultat = nombre_entier + nombre_flottant
print("Le résultat de l'addition est:", resultat) #output: 30.5